// Helper functions
export const getStoriesByCategory = (categoryId: string) => {
  return STORIES.filter(story => story.category === categoryId);
};

export const searchStories = (query: string) => {
  const lowercaseQuery = query.toLowerCase();
  return STORIES.filter(story =>
    story.title.toLowerCase().includes(lowercaseQuery) ||
    story.story.toLowerCase().includes(lowercaseQuery)
  );
};

export const getRecommendedStories = (currentStory: typeof STORIES[0], limit = 3) => {
  return STORIES
    .filter(story => story.id !== currentStory.id && story.category === currentStory.category)
    .slice(0, limit);
};